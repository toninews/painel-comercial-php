CREATE OR REPLACE VIEW view_saldo_pessoa AS
SELECT
    id,
    nome,
    endereco,
    bairro,
    telefone,
    email,
    (SELECT SUM(valor) FROM conta WHERE id_cliente = pessoa.id) AS total,
    (SELECT SUM(valor) FROM conta WHERE id_cliente = pessoa.id AND paga = 'N') AS aberto
FROM pessoa
ORDER BY 8 DESC;

